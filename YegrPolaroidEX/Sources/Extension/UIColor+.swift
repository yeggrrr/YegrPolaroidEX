import UIKit

extension UIColor {
    static var customPointColor: UIColor {
        return UIColor(named: "CustomPointColor") ?? .systemPurple
    }
    
    static var pointDarkColor: UIColor {
        return .systemPurple
    }
    
    static var incompleteColor: UIColor {
        return .systemGray2
    }
}
